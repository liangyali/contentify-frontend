  methods: {
    handleSubmit(formName) {
      this.$refs[formName].validate((valid) => {
        if (!valid) {
          return;
        }

        this.loading = true;
        this.$auth
          .loginWith("local", {
            data: {
              ...this.form,
            },
          })
          .then(() => {})
          .catch((err) => {
            this.$message.error("用户名或密码错误");
          })
          .finally(() => {
            this.loading = false;
          });
      });
